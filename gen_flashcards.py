class QuotedDumper(yaml.SafeDumper):
    pass

def repr_str(dumper, data):
    return dumper.represent_scalar("tag:yaml.org,2002:str", data, style='"')

QuotedDumper.add_representer(str, repr_str)

for fn, deck in decks.items():
    cards_out = []
    for cid, front, back, tags in deck["cards"]:
        cards_out.append({"id": cid, "front": front, "back": back, "tags": tags})
    out = {"conceptId": deck["conceptId"], "cards": cards_out}
    path = "topics/fastapi/flashcards/" + fn
    with open(path, "w", encoding="utf-8") as f:
        yaml.dump(out, f, Dumper=QuotedDumper, allow_unicode=True, sort_keys=False, width=4096)
    print("wrote", path)
